    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
    try {
        // TODO add your handling code here:
        String typeValue = (String) type.getSelectedItem();
        String statusValue = (String) status.getSelectedItem();

        // Check if values are not null before setting them in the prepared statement
        if (typeValue != null && statusValue != null) {
            pst = con.prepareStatement("UPDATE supplier SET factory_name=?,s_date=?,focal_person=?,address=?,bank=?,branch=?,account_no=?,contact=?,s_type=?,s_status=? WHERE s_id=?");
            pst.setString(1, factory_name.getText());
            try {
                pst.setDate(2, java.sql.Date.valueOf(date.getText()));  // Assuming date is in the format "yyyy-MM-dd"
            } catch (IllegalArgumentException e) {
                JOptionPane.showMessageDialog(this, "Invalid date format. Please enter the date in the format yyyy-MM-dd.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
                // Optionally, you can clear the date field or take other corrective actions
                date.setText("");
                return;  // Exit the method to avoid further processing
            }
            pst.setString(3, focal_person.getText());
            pst.setString(4, address.getText());
            pst.setString(5, bank.getText());
            pst.setString(6, branch.getText());
            pst.setString(7, account.getText());
            pst.setString(8, contact.getText());
            pst.setString(9, typeValue);
            pst.setString(10, statusValue);
            pst.setString(11, s_id.getText());  // This should be the last parameter

            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Update successful");
        } else {
            // Handle the case where one or both values are null
            // You may want to provide default values or handle it according to your application logic
            // For example:
            JOptionPane.showMessageDialog(this, "Type or Status is null");
        }
    } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(supplier.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_editActionPerformed

