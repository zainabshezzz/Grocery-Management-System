// Method to check if a supplier with the given ID already exists
private boolean isSupplierIdExists(int supplierId) {

        try {
            pst = con.prepareStatement("SELECT * FROM supplier WHERE s_id = ?");
            pst.setInt(1, supplierId);
            ResultSet rs = pst.executeQuery();
            return rs.next();  // Returns true if a record is found
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(supplier.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            return false;
        }
}
    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
    int supplierId = Integer.parseInt(s_id.getText());

    // Check if the supplierId already exists in the 'supplier' table
    if (isSupplierIdExists(supplierId)) {
        JOptionPane.showMessageDialog(this, "Supplier with ID " + supplierId + " already exists.", "Duplicate Entry", JOptionPane.ERROR_MESSAGE);
        // Optionally, you can clear the supplier_id field or take other corrective actions
        s_id.setText("");
        return;  // Exit the method to avoid further processing
    }

    try {
        pst = con.prepareStatement("INSERT INTO supplier(s_id,factory_name,focal_person,s_date,address,bank,branch,account_no,contact,s_type,s_status) VALUES (?, ?, ?,?,?,?,?,?,?,?,?)");
        pst.setInt(1, supplierId);
        pst.setString(2,factory_name.getText());
        pst.setString(3,focal_person.getText());
        try {
            pst.setDate(4, java.sql.Date.valueOf(date.getText()));  // Assuming date is in the format "yyyy-MM-dd"
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Invalid date format. Please enter the date in the format yyyy-MM-dd.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            // Optionally, you can clear the date field or take other corrective actions
            date.setText("");
            return;  // Exit the method to avoid further processing
        }
        pst.setString(5,address.getText());
        pst.setString(6,bank.getText());
        pst.setString(7,branch.getText());
        pst.setString(8, account.getText());
        pst.setString(9,contact.getText());
        pst.setString(10, type.getSelectedItem().toString());
        pst.setString(11, status.getSelectedItem().toString());
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Success");

        // Your existing code for resetting fields
        reset();
    } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(supplier.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Invalid supplier ID. Please enter a valid integer.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        // Optionally, you can clear the supplier_id field or take other corrective actions
        s_id.setText("");
    }
    }//GEN-LAST:event_saveActionPerformed

